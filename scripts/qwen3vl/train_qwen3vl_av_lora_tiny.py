#!/usr/bin/env python3
"""Tiny AV SFT for Qwen3-VL NLA: injected activation -> explanation text.

This is a deliberately small overfit-style experiment. It reads an `av_sft`
parquet generated by `extract_qwen3vl_layer15_dataset.py`, replaces repeated
`<|image_pad|>` marker embeddings with a layer activation vector, and trains
LoRA weights on the Qwen3-VL language model to emit the target
`<explanation>...</explanation>`.

It is not production NLA training, but it closes the first AV loop with the same
mechanism NLA needs: activation-as-input-embedding.
"""

from __future__ import annotations

import argparse
import json
import math
from pathlib import Path

import numpy as np
import pyarrow.parquet as pq
import torch
import torch.nn.functional as F
import yaml
from peft import LoraConfig, get_peft_model
from transformers import AutoProcessor, Qwen3VLForConditionalGeneration


def sidecar_path(path: str) -> Path:
    return Path(path + ".nla_meta.yaml")


def normalize(v: torch.Tensor, scale: float | None) -> torch.Tensor:
    if scale is None:
        return v
    return v / v.float().norm(dim=-1, keepdim=True).clamp_min(1e-12) * scale


def unwrap_model(model):
    return model.get_base_model() if hasattr(model, "get_base_model") else model


def load_rows(path: str, max_rows: int | None = None) -> list[dict]:
    table = pq.read_table(path)
    if max_rows is not None:
        table = table.slice(0, max_rows)
    return table.to_pylist()


def expand_injection_prompt(prompt: str, injection_token: str, num_injection_tokens: int) -> str:
    if num_injection_tokens == 1:
        return prompt
    if prompt.count(injection_token) != 1:
        raise RuntimeError(f"expected one textual injection token {injection_token!r}")
    return prompt.replace(injection_token, injection_token * num_injection_tokens, 1)


def find_injection_positions(ids: list[int], inj_id: int, left_id: int, right_id: int, num_injection_tokens: int) -> list[int]:
    matches = [i for i, tid in enumerate(ids) if tid == inj_id]
    if len(matches) != num_injection_tokens:
        raise RuntimeError(f"expected {num_injection_tokens} injection tokens, got {len(matches)}")
    start = matches[0]
    expected = list(range(start, start + num_injection_tokens))
    if matches != expected:
        raise RuntimeError(f"injection tokens are not contiguous: {matches}")
    if ids[start - 1] != left_id or ids[start + num_injection_tokens] != right_id:
        raise RuntimeError("injection neighbors do not match sidecar")
    return matches


def encode_example(
    tokenizer,
    row: dict,
    inj_id: int,
    left_id: int,
    right_id: int,
    injection_token: str,
    num_injection_tokens: int,
) -> dict:
    prompt = expand_injection_prompt(row["prompt"], injection_token, num_injection_tokens)
    prompt_ids = tokenizer.encode(prompt, add_special_tokens=False)
    inj_positions = find_injection_positions(prompt_ids, inj_id, left_id, right_id, num_injection_tokens)
    response_ids = tokenizer.encode(row["response"], add_special_tokens=False)
    if tokenizer.eos_token_id is not None:
        response_ids = response_ids + [tokenizer.eos_token_id]
    input_ids = torch.tensor(prompt_ids + response_ids, dtype=torch.long)
    labels = torch.full_like(input_ids, -100)
    labels[len(prompt_ids) :] = torch.tensor(response_ids, dtype=torch.long)
    activation = torch.tensor(row["activation_vector"], dtype=torch.float32)
    return {
        "input_ids": input_ids,
        "labels": labels,
        "activation": activation,
        "inj_positions": inj_positions,
        "prompt_len": len(prompt_ids),
        "response": row["response"],
        "source_description": row.get("source_description", ""),
    }


def map_activation(
    activation_adapter: torch.nn.Module | None,
    activation: torch.Tensor,
    injection_scale: float | None,
    num_injection_tokens: int,
    device: torch.device,
    dtype: torch.dtype,
) -> torch.Tensor:
    activation = normalize(activation.view(1, -1), injection_scale).to(device)
    if activation_adapter is not None:
        mapped = activation_adapter(activation.float()).view(activation.shape[0], num_injection_tokens, -1)
    else:
        mapped = activation.view(activation.shape[0], 1, -1).expand(-1, num_injection_tokens, -1)
        if num_injection_tokens > 1:
            mapped = mapped / math.sqrt(num_injection_tokens)
    return mapped.to(device, dtype)


def build_activation_adapter(d_model: int, num_injection_tokens: int, device: torch.device) -> torch.nn.Linear:
    adapter = torch.nn.Linear(d_model, d_model * num_injection_tokens, bias=True, device=device, dtype=torch.float32)
    with torch.no_grad():
        adapter.weight.zero_()
        scale = 1.0 / math.sqrt(num_injection_tokens)
        eye = torch.eye(d_model, device=device, dtype=torch.float32) * scale
        for i in range(num_injection_tokens):
            adapter.weight[i * d_model : (i + 1) * d_model].copy_(eye)
        adapter.bias.zero_()
    return adapter


def expected_adapter_weight(d_model: int, num_injection_tokens: int) -> torch.Tensor:
    weight = torch.zeros((d_model * num_injection_tokens, d_model), dtype=torch.float32)
    eye = torch.eye(d_model, dtype=torch.float32) / math.sqrt(num_injection_tokens)
    for i in range(num_injection_tokens):
        weight[i * d_model : (i + 1) * d_model].copy_(eye)
    return weight


def forward_loss(
    model,
    activation_adapter: torch.nn.Module | None,
    encoded: dict,
    injection_scale: float | None,
    device: torch.device,
    activation_override: torch.Tensor | None = None,
) -> torch.Tensor:
    base = unwrap_model(model)
    input_ids = encoded["input_ids"].to(device).unsqueeze(0)
    labels = encoded["labels"].to(device).unsqueeze(0)
    attention_mask = torch.ones_like(input_ids, device=device)
    with torch.no_grad():
        embeds = base.get_input_embeddings()(input_ids)
    inj_positions = encoded["inj_positions"]
    activation = map_activation(
        activation_adapter,
        encoded["activation"] if activation_override is None else activation_override,
        injection_scale,
        len(inj_positions),
        device,
        embeds.dtype,
    )
    embeds = embeds.clone()
    embeds[0, inj_positions] = activation[0]
    out = base.model(
        input_ids=None,
        inputs_embeds=embeds,
        attention_mask=attention_mask,
        use_cache=False,
        return_dict=True,
    )
    logits = base.lm_head(out.last_hidden_state)
    shift_logits = logits[:, :-1, :].contiguous().float()
    shift_labels = labels[:, 1:].contiguous()
    return F.cross_entropy(
        shift_logits.view(-1, shift_logits.shape[-1]),
        shift_labels.view(-1),
        ignore_index=-100,
    )


@torch.no_grad()
def greedy_generate(
    model,
    activation_adapter: torch.nn.Module | None,
    tokenizer,
    encoded: dict,
    injection_scale: float | None,
    device: torch.device,
    max_new_tokens: int,
) -> str:
    base = unwrap_model(model)
    prompt_ids = encoded["input_ids"][: encoded["prompt_len"]].tolist()
    generated: list[int] = []
    eos = tokenizer.eos_token_id
    for _ in range(max_new_tokens):
        ids = torch.tensor(prompt_ids + generated, dtype=torch.long, device=device).unsqueeze(0)
        attention_mask = torch.ones_like(ids, device=device)
        embeds = base.get_input_embeddings()(ids)
        inj_positions = encoded["inj_positions"]
        activation = map_activation(
            activation_adapter,
            encoded["activation"],
            injection_scale,
            len(inj_positions),
            device,
            embeds.dtype,
        )
        embeds = embeds.clone()
        embeds[0, inj_positions] = activation[0]
        out = base.model(
            input_ids=None,
            inputs_embeds=embeds,
            attention_mask=attention_mask,
            use_cache=False,
            return_dict=True,
        )
        logits = base.lm_head(out.last_hidden_state[:, -1, :]).float()
        next_id = int(torch.argmax(logits, dim=-1).item())
        if eos is not None and next_id == eos:
            break
        generated.append(next_id)
    return tokenizer.decode(generated, skip_special_tokens=False)


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--av-parquet", required=True)
    parser.add_argument("--model-id", default="Qwen/Qwen3-VL-8B-Instruct")
    parser.add_argument("--out-dir", required=True)
    parser.add_argument("--max-rows", type=int, default=64)
    parser.add_argument("--epochs", type=int, default=2)
    parser.add_argument("--lr", type=float, default=2e-4)
    parser.add_argument("--lora-r", type=int, default=8)
    parser.add_argument("--lora-alpha", type=int, default=16)
    parser.add_argument("--lora-dropout", type=float, default=0.05)
    parser.add_argument("--target-modules", default="q_proj,v_proj")
    parser.add_argument("--injection-scale", type=float, default=57.75)
    parser.add_argument("--grad-accum", type=int, default=8)
    parser.add_argument("--max-new-tokens", type=int, default=80)
    parser.add_argument("--train-activation-adapter", action="store_true")
    parser.add_argument("--activation-adapter-lr", type=float, default=None)
    parser.add_argument("--num-injection-tokens", type=int, default=1)
    parser.add_argument("--contrastive-shuffle-weight", type=float, default=0.0)
    parser.add_argument("--contrastive-margin", type=float, default=0.02)
    parser.add_argument("--response-contrastive-weight", type=float, default=0.0)
    parser.add_argument("--response-contrastive-margin", type=float, default=0.02)
    parser.add_argument("--seed", type=int, default=7)
    parser.add_argument("--local-files-only", action=argparse.BooleanOptionalAction, default=True)
    args = parser.parse_args()

    torch.manual_seed(args.seed)
    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    meta = yaml.safe_load(sidecar_path(args.av_parquet).read_text())
    token_meta = meta["tokens"]
    rows = load_rows(args.av_parquet, args.max_rows)

    processor = AutoProcessor.from_pretrained(
        args.model_id,
        trust_remote_code=True,
        local_files_only=args.local_files_only,
    )
    tokenizer = processor.tokenizer
    left_id = int(token_meta.get("injection_left_neighbor_id", token_meta.get("vision_start_token_id")))
    right_id = int(token_meta.get("injection_right_neighbor_id", token_meta.get("vision_end_token_id")))
    encoded = [
        encode_example(
            tokenizer,
            row,
            int(token_meta["injection_token_id"]),
            left_id,
            right_id,
            str(token_meta.get("injection_token", "<|image_pad|>")),
            args.num_injection_tokens,
        )
        for row in rows
    ]

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    model = Qwen3VLForConditionalGeneration.from_pretrained(
        args.model_id,
        trust_remote_code=True,
        local_files_only=args.local_files_only,
        torch_dtype=torch.bfloat16,
        low_cpu_mem_usage=True,
    ).to(device)
    model.config.use_cache = False

    lora_config = LoraConfig(
        r=args.lora_r,
        lora_alpha=args.lora_alpha,
        lora_dropout=args.lora_dropout,
        bias="none",
        task_type="CAUSAL_LM",
        target_modules=[x.strip() for x in args.target_modules.split(",") if x.strip()],
    )
    model = get_peft_model(model, lora_config)
    for name, param in model.named_parameters():
        if "visual" in name or "vision_tower" in name or "multi_modal_projector" in name:
            param.requires_grad = False
    model.train()

    activation_adapter = None
    if args.train_activation_adapter:
        d_model = int(encoded[0]["activation"].numel())
        activation_adapter = build_activation_adapter(d_model, args.num_injection_tokens, device)
        activation_adapter.train()

    model_params = [p for p in model.parameters() if p.requires_grad]
    adapter_params = list(activation_adapter.parameters()) if activation_adapter is not None else []
    model_trainable = sum(p.numel() for p in model_params)
    adapter_trainable = sum(p.numel() for p in adapter_params)
    trainable = model_trainable + adapter_trainable
    total = sum(p.numel() for p in model.parameters()) + sum(p.numel() for p in adapter_params)
    if adapter_params and args.activation_adapter_lr is not None:
        opt = torch.optim.AdamW(
            [
                {"params": model_params, "lr": args.lr},
                {"params": adapter_params, "lr": args.activation_adapter_lr},
            ]
        )
    else:
        opt = torch.optim.AdamW(model_params + adapter_params, lr=args.lr)
    params_to_clip = model_params + adapter_params

    losses: list[float] = []
    sft_losses: list[float] = []
    contrastive_losses: list[float] = []
    response_contrastive_losses: list[float] = []
    epoch_summaries = []
    order = torch.randperm(len(encoded)).tolist()
    step = 0
    opt.zero_grad(set_to_none=True)
    for epoch in range(args.epochs):
        if epoch > 0:
            order = torch.randperm(len(encoded)).tolist()
        epoch_losses = []
        epoch_sft_losses = []
        epoch_contrastive_losses = []
        epoch_response_contrastive_losses = []
        for i, idx in enumerate(order):
            pos_loss = forward_loss(model, activation_adapter, encoded[idx], args.injection_scale, device)
            loss = pos_loss
            contrastive_loss = torch.zeros((), device=device, dtype=pos_loss.dtype)
            response_contrastive_loss = torch.zeros((), device=device, dtype=pos_loss.dtype)
            neg_idx = None
            if (args.contrastive_shuffle_weight > 0.0 or args.response_contrastive_weight > 0.0) and len(encoded) > 1:
                neg_idx = order[(i + 1) % len(order)]
                if neg_idx == idx:
                    neg_idx = order[(i + 2) % len(order)]
            if args.contrastive_shuffle_weight > 0.0 and neg_idx is not None:
                neg_loss = forward_loss(
                    model,
                    activation_adapter,
                    encoded[idx],
                    args.injection_scale,
                    device,
                    activation_override=encoded[neg_idx]["activation"],
                )
                margin = torch.tensor(args.contrastive_margin, device=device, dtype=pos_loss.dtype)
                contrastive_loss = F.relu(margin - (neg_loss - pos_loss))
                loss = pos_loss + args.contrastive_shuffle_weight * contrastive_loss
            if args.response_contrastive_weight > 0.0 and neg_idx is not None:
                wrong_response_loss = forward_loss(
                    model,
                    activation_adapter,
                    encoded[neg_idx],
                    args.injection_scale,
                    device,
                    activation_override=encoded[idx]["activation"],
                )
                response_margin = torch.tensor(args.response_contrastive_margin, device=device, dtype=pos_loss.dtype)
                response_contrastive_loss = F.relu(response_margin - (wrong_response_loss - pos_loss))
                loss = loss + args.response_contrastive_weight * response_contrastive_loss
            (loss / args.grad_accum).backward()
            losses.append(float(loss.detach().cpu().item()))
            sft_losses.append(float(pos_loss.detach().cpu().item()))
            contrastive_losses.append(float(contrastive_loss.detach().cpu().item()))
            response_contrastive_losses.append(float(response_contrastive_loss.detach().cpu().item()))
            epoch_losses.append(losses[-1])
            epoch_sft_losses.append(sft_losses[-1])
            epoch_contrastive_losses.append(contrastive_losses[-1])
            epoch_response_contrastive_losses.append(response_contrastive_losses[-1])
            if (i + 1) % args.grad_accum == 0 or i == len(order) - 1:
                torch.nn.utils.clip_grad_norm_(params_to_clip, 1.0)
                opt.step()
                opt.zero_grad(set_to_none=True)
                step += 1
        model.eval()
        if activation_adapter is not None:
            activation_adapter.eval()
        generations = []
        for sample_idx in range(min(4, len(encoded))):
            text = greedy_generate(
                model,
                activation_adapter,
                tokenizer,
                encoded[sample_idx],
                args.injection_scale,
                device,
                args.max_new_tokens,
            )
            generations.append(
                {
                    "sample_index": sample_idx,
                    "target": encoded[sample_idx]["response"],
                    "generated": text,
                    "source_description": encoded[sample_idx]["source_description"],
            }
        )
        model.train()
        if activation_adapter is not None:
            activation_adapter.train()
        epoch_summaries.append(
            {
                "epoch": epoch,
                "mean_loss": float(np.mean(epoch_losses)),
                "mean_sft_loss": float(np.mean(epoch_sft_losses)),
                "mean_contrastive_loss": float(np.mean(epoch_contrastive_losses)),
                "mean_response_contrastive_loss": float(np.mean(epoch_response_contrastive_losses)),
                "last_loss": float(epoch_losses[-1]),
                "generations": generations,
            }
        )

    model.eval()
    if activation_adapter is not None:
        activation_adapter.eval()
    final_losses = []
    with torch.no_grad():
        for ex in encoded:
            final_losses.append(
                float(forward_loss(model, activation_adapter, ex, args.injection_scale, device).detach().cpu().item())
            )

    model.save_pretrained(out_dir / "adapter")
    activation_adapter_summary = None
    if activation_adapter is not None:
        adapter_path = out_dir / "activation_adapter.pt"
        with torch.no_grad():
            weight = activation_adapter.weight.detach().float().cpu()
            bias = activation_adapter.bias.detach().float().cpu()
            expected_weight = expected_adapter_weight(
                int(encoded[0]["activation"].numel()),
                args.num_injection_tokens,
            )
            activation_adapter_summary = {
                "path": str(adapter_path),
                "type": "linear_repeat_identity_init",
                "d_model": int(encoded[0]["activation"].numel()),
                "num_injection_tokens": args.num_injection_tokens,
                "out_features": int(weight.shape[0]),
                "param_count": adapter_trainable,
                "lr": args.activation_adapter_lr if args.activation_adapter_lr is not None else args.lr,
                "weight_delta_fro_norm": float((weight - expected_weight).norm().item()),
                "bias_norm": float(bias.norm().item()),
            }
            torch.save(
                {
                    "type": "linear_repeat_identity_init",
                    "d_model": int(encoded[0]["activation"].numel()),
                    "num_injection_tokens": args.num_injection_tokens,
                    "state_dict": activation_adapter.state_dict(),
                    "summary": activation_adapter_summary,
                },
                adapter_path,
            )
    summary = {
        "av_parquet": args.av_parquet,
        "model_id": args.model_id,
        "num_rows": len(encoded),
        "epochs": args.epochs,
        "lr": args.lr,
        "lora_r": args.lora_r,
        "lora_alpha": args.lora_alpha,
        "lora_dropout": args.lora_dropout,
        "target_modules": args.target_modules,
        "injection_scale": args.injection_scale,
        "num_injection_tokens": args.num_injection_tokens,
        "prompt_token_count": int(encoded[0]["prompt_len"]),
        "injection_positions": [int(x) for x in encoded[0]["inj_positions"]],
        "grad_accum": args.grad_accum,
        "contrastive_shuffle_weight": args.contrastive_shuffle_weight,
        "contrastive_margin": args.contrastive_margin,
        "response_contrastive_weight": args.response_contrastive_weight,
        "response_contrastive_margin": args.response_contrastive_margin,
        "train_activation_adapter": args.train_activation_adapter,
        "activation_adapter_lr": args.activation_adapter_lr if args.activation_adapter_lr is not None else args.lr,
        "activation_adapter": activation_adapter_summary,
        "model_trainable_params": model_trainable,
        "activation_adapter_trainable_params": adapter_trainable,
        "trainable_params": trainable,
        "total_params": total,
        "trainable_fraction": trainable / total,
        "initial_loss_mean_first_8": float(np.mean(losses[: min(8, len(losses))])),
        "initial_sft_loss_mean_first_8": float(np.mean(sft_losses[: min(8, len(sft_losses))])),
        "initial_contrastive_loss_mean_first_8": float(
            np.mean(contrastive_losses[: min(8, len(contrastive_losses))])
        ),
        "initial_response_contrastive_loss_mean_first_8": float(
            np.mean(response_contrastive_losses[: min(8, len(response_contrastive_losses))])
        ),
        "final_eval_loss_mean": float(np.mean(final_losses)),
        "final_eval_loss_std": float(np.std(final_losses)),
        "losses": losses,
        "sft_losses": sft_losses,
        "contrastive_losses": contrastive_losses,
        "response_contrastive_losses": response_contrastive_losses,
        "epoch_summaries": epoch_summaries,
    }
    (out_dir / "summary.json").write_text(json.dumps(summary, indent=2), encoding="utf-8")
    omitted = {"losses", "sft_losses", "contrastive_losses", "response_contrastive_losses", "epoch_summaries"}
    print(json.dumps({k: v for k, v in summary.items() if k not in omitted}, indent=2))
    print(json.dumps({"epoch_summaries": epoch_summaries}, indent=2))


if __name__ == "__main__":
    main()
